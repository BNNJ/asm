#include "libft.h"

int				ft_countnb(intmax_t n)
{
	intmax_t	i;

	i = 0;
	if (n < 0)
	{
		n *= (-1);
		i++;
	}
	if (n == 0)
		i = 1;
	while (n > 0)
	{
		i++;
		n /= 10;
	}
	return (i);
}
