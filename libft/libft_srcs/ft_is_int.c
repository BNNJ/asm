#include "libft.h"

int				ft_is_int(intmax_t n, int neg)
{
	if (neg == 1 && n >= -2147483648 && n <= 0)
		return (1);
	else if (neg == 0 && n >= -2147483648 && n <= 2147483647)
		return (1);
	else if (neg == -1 && n >= 0 && n <= 2147483647)
		return (1);
	else
		return (0);
}
